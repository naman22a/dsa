void printArray(vector<int> arr)
{
    for (int i = 0; i < arr.size(); i++)
        cout << arr[i] << " ";
}

void printArray(vector<vector<int>> arr)
{
    for (int i = 0; i < arr.size(); i++)
    {
        cout << "{ ";
        for (int j = 0; j < arr[i].size(); j++)
            cout << arr[i][j] << " ";
        cout << " }" << endl;
    }
}
