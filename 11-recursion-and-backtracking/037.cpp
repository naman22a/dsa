// Leetcode 78: Subsets
// power set -> set of all subsets

void backtrack(vector<int> nums, vector<vector<int>> &result, vector<int> subset, int index);

// TC: O(N.(2^N))
// SC: O(N)
vector<vector<int>> subsets(vector<int> &nums)
{
    // number of subsets = 2^n
    // solve using recursion/backtracking
    // use decision tree

    vector<vector<int>> result;
    vector<int> subset; // current subset

    backtrack(nums, result, subset, 0);

    return result;
}

// i -> index of the value that we are making the decision on
void backtrack(vector<int> nums, vector<vector<int>> &result, vector<int> subset, int index)
{
    // base case: index out of bounds
    if (index >= nums.size())
    {
        result.push_back(subset);
        return;
    }

    // decision to include nums[index]
    subset.push_back(nums[index]);
    backtrack(nums, result, subset, index + 1);

    // decision to exclude nums[index]
    subset.pop_back();
    backtrack(nums, result, subset, index + 1);
}

